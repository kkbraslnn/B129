package interviewquestions;

public class InterviewQuestion06 {
    public static void main(String[] args) {
        //Interview question(very common)
        //ex2: Find the sum of the integers from 3 to 6

        int sum = 0;
        for(int i=3; i<7; i++){

            sum = sum + i;

        }

        System.out.println(sum);
    }
}
